package com.liferay.portal.search.web.internal.page;

import com.liferay.portal.configuration.metatype.bnd.util.ConfigurableUtil;
import com.liferay.portal.kernel.util.Portal;
import com.liferay.portal.search.web.configuration.DefaultSearchPageConfiguration;

import java.util.Map;

import org.osgi.service.component.annotations.Activate;
import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Modified;
import org.osgi.service.component.annotations.Reference;

/**
 * @author Andr√© de Oliveira
 */
@Component(
	configurationPid = "com.liferay.portal.search.web.configuration.DefaultSearchPageConfiguration",
	immediate = true, service = DefaultSearchPageAutogenerator.class
)
public class DefaultSearchPageAutogenerator {

	public void generate(long companyId) throws Exception {
		if (!_autogenerateDefaultSearchPage) {
			return;
		}

		if (companyId != portal.getDefaultCompanyId()) {
			return;
		}

		defaultSearchPageGenerator.generate(companyId);
	}

	@Activate
	@Modified
	protected void activate(Map<String, Object> properties) {
		DefaultSearchPageConfiguration defaultSearchPageConfiguration =
			ConfigurableUtil.createConfigurable(
				DefaultSearchPageConfiguration.class, properties);

		setAutogenerateDefaultSearchPage(
			defaultSearchPageConfiguration.autogenerateDefaultSearchPage());
	}

	protected void setAutogenerateDefaultSearchPage(
		boolean autogenerateDefaultSearchPage) {

		_autogenerateDefaultSearchPage = autogenerateDefaultSearchPage;
	}

	@Reference
	protected DefaultSearchPageGenerator defaultSearchPageGenerator;

	@Reference
	protected Portal portal;

	private boolean _autogenerateDefaultSearchPage;

}